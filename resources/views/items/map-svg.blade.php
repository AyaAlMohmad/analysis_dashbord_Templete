    <script>
        document.getElementById('exportButton').addEventListener('click', function() {
            const element = document.getElementById('reportContent');
            const exportButtonContainer = document.getElementById('exportButton-container');

            if (!element) {
                alert("Report content element not found!");
                return;
            }

            //  Get authenticated user's name from meta tag
            const userName = document.querySelector('meta[name="user-name"]').getAttribute('content') ||
                'Unknown User';

            //  Create a footer note with the date and user name
            const footerNote = document.createElement('div');
            const today = new Date();
            const formattedDate = today.toLocaleDateString('en-GB'); // You can use toLocaleString() for date + time
            footerNote.innerHTML = ` by: ${userName}<br>  Exported on: ${formattedDate} `;
            footerNote.style.marginTop = "20px";
            footerNote.style.marginBottom = "400px";
            footerNote.style.fontSize = "12px";
            footerNote.style.textAlign = "center";
            footerNote.style.color = "#555";

            //  Append the footer note temporarily
            element.appendChild(footerNote);

            exportButtonContainer.style.display = 'none';

            html2canvas(element, {
                scale: 2,
                useCORS: true
            }).then(canvas => {
                const imgData = canvas.toDataURL('image/jpeg', 1.0);
                const pdf = new jspdf.jsPDF('p', 'mm', 'a4');

                const imgProps = pdf.getImageProperties(imgData);
                const pdfWidth = pdf.internal.pageSize.getWidth();
                const pdfHeight = (imgProps.height * pdfWidth) / imgProps.width;

                pdf.addImage(imgData, 'JPEG', 0, 0, pdfWidth, pdfHeight);
                pdf.save('report.pdf');

                exportButtonContainer.style.display = 'block';

                //  Remove the temporary footer
                element.removeChild(footerNote);
            });
        });
    </script>
